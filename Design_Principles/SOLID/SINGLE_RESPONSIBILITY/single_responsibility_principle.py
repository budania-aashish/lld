"""
    Single Responsibility Principle (SRP)
    - A class should have one, and only one, reason to change.
    - This means that a class must have only one responsibility.

    When a class performs just one task, it contains a small number
    of methods and member variables making them more usable and easier to maintain.

    If a class has multiple responsibilities, it becomes harder to understand, maintain,
    and modify and increases the potential for bugs because changes to one responsibility
    could affect the others.


"""