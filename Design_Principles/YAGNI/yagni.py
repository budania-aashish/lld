"""
    YAGNI

    In the fast-paced world of software development, there's an innate temptation to build for the future.

    We want our code to handle every imaginable scenario and cater to a myriad of potential features.

    However, this mindset can easily lead to bloated codebases, wasted development time, and increased
    complexity. This is where the YAGNI short for "You Aren't Gonna Need It" comes to the rescue.

   YAGNI:- Always implement things when you actually need them, never when you just foresee that you might need them.

   This focuses on simplicity and incremental development.

   At its heart, YAGNI is about avoiding over-engineering and focusing strictly on the present requirements.

   The rationale behind YAGNI is simple: every line of code we write comes with a cost. It needs to be developed,
   tested, maintained, and understood by other developers.

   By adding unnecessary features or over-engineering solutions, we incur additional complexity, which can slow
   down development, increase the likelihood of bugs, and make the codebase harder to maintain.

"""


